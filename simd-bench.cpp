__attribute__((optimize("no-tree-vectorize", "no-unroll-loops")))
int64_t findMaxScalar(std::vector<int64_t>& arr)
{
    int64_t* pArr = arr.data();
    size_t arrSize = arr.size();
    int64_t max = pArr[0];

    for (size_t i = 0; i < arrSize; ++i) {
        if (max < pArr[i])
            max = pArr[i];
    }
    return max;
}

__attribute__((optimize("no-tree-vectorize", "no-unroll-loops")))
int64_t findMaxScalar_unrolled(std::vector<int64_t>& arr)
{
    int64_t* pArr = arr.data();
    size_t arrSize = arr.size();
    if (arrSize == 0) return 0;

    int64_t max0 = pArr[0];
    int64_t max1 = pArr[0];
    int64_t max2 = pArr[0];
    int64_t max3 = pArr[0];

    size_t i = 0;
    for (; i + 4 <= arrSize; i += 4) {
        if (pArr[i] > max0) max0 = pArr[i];
        if (pArr[i + 1] > max1) max1 = pArr[i + 1];
        if (pArr[i + 2] > max2) max2 = pArr[i + 2];
        if (pArr[i + 3] > max3) max3 = pArr[i + 3];
    }

    int64_t max = std::max({max0, max1, max2, max3});
    for (; i < arrSize; ++i) {
        if (pArr[i] > max) max = pArr[i];
    }
    return max;
}

int64_t findMax_simd_portable(const std::vector<int64_t>& arr)
{
    // Automatically picks the best SIMD width for the target architecture
    using simd_t = stdx::native_simd<int64_t>;
    constexpr size_t width = simd_t::size();

    size_t i = 0;
    simd_t max_v(arr[0]); // Broadcast the first element across all vector lanes

    // Main vector accumulation loop
    for (; i + width <= arr.size(); i += width) {
        simd_t chunk;
        chunk.copy_from(&arr[i], stdx::element_aligned);
        max_v = stdx::max(max_v, chunk); // Element-wise vertical maximum
    }

    // Horizontal reduction down to a single scalar value
    int64_t max_val = stdx::hmax(max_v);

    // Scalar cleanup loop for any remaining trailing elements
    for (; i < arr.size(); ++i) {
        max_val = std::max(max_val, arr[i]);
    }

    return max_val;
}

int64_t findMax_simd_portable_unrolled(const std::vector<int64_t>& arr)
{
    using simd_t = stdx::native_simd<int64_t>;
    constexpr size_t width = simd_t::size();
    constexpr size_t unroll = 4;
    const size_t totalWidth = width * unroll;

    size_t i = 0;
    simd_t max_v0(arr[0]);
    simd_t max_v1(arr[0]);
    simd_t max_v2(arr[0]);
    simd_t max_v3(arr[0]);

    for (; i + totalWidth <= arr.size(); i += totalWidth) {
        simd_t c0, c1, c2, c3;
        c0.copy_from(&arr[i], stdx::element_aligned);
        c1.copy_from(&arr[i + width], stdx::element_aligned);
        c2.copy_from(&arr[i + 2 * width], stdx::element_aligned);
        c3.copy_from(&arr[i + 3 * width], stdx::element_aligned);
        max_v0 = stdx::max(max_v0, c0);
        max_v1 = stdx::max(max_v1, c1);
        max_v2 = stdx::max(max_v2, c2);
        max_v3 = stdx::max(max_v3, c3);
    }

    simd_t max_v = stdx::max(stdx::max(max_v0, max_v1), stdx::max(max_v2, max_v3));

    for (; i + width <= arr.size(); i += width) {
        simd_t chunk;
        chunk.copy_from(&arr[i], stdx::element_aligned);
        max_v = stdx::max(max_v, chunk);
    }

    int64_t max_val = stdx::hmax(max_v);

    for (; i < arr.size(); ++i) {
        max_val = std::max(max_val, arr[i]);
    }

    return max_val;
}

int64_t findMax_simd_avx512(const std::vector<int64_t>& arr)
{
    // Force exactly 8 lanes to map directly to an AVX-512 ZMM register
    using simd_t = stdx::fixed_size_simd<int64_t, 8>;
    using mask_t = typename simd_t::mask_type;
    constexpr size_t width = simd_t::size();

    size_t i = 0;
    simd_t max_v(arr[0]);

    // Main bulk loop (processes chunks of 8 elements)
    for (; i + width <= arr.size(); i += width) {
        simd_t chunk;
        chunk.copy_from(&arr[i], stdx::element_aligned);
        max_v = stdx::max(max_v, chunk);
    }

    // Advanced AVX-512 Optimization: Clean up the tail using hardware masking
    if (i < arr.size()) {
        size_t remainder = arr.size() - i;

        // 1. Generate an index vector: [0, 1, 2, 3, 4, 5, 6, 7]
        simd_t indices([](size_t idx) { return static_cast<int64_t>(idx); });

        // 2. Create the mask by comparing the indices to the remaining size
        mask_t tail_mask = indices < static_cast<int64_t>(remainder);

        // Initialize a dummy vector with the lowest possible values 
        simd_t chunk(std::numeric_limits<int64_t>::lowest());

        // Conditionally load the final partial elements into the active lanes
        stdx::where(tail_mask, chunk).copy_from(&arr[i], stdx::element_aligned);

        // One final vertical max comparison
        max_v = stdx::max(max_v, chunk);
    }

    // Final horizontal reduction
    return stdx::hmax(max_v);
}

int64_t findMax_simd_avx512_unrolled(const std::vector<int64_t>& arr)
{
    using simd_t = stdx::fixed_size_simd<int64_t, 8>;
    using mask_t = typename simd_t::mask_type;
    constexpr size_t width = simd_t::size();
    constexpr size_t unroll = 4;
    const size_t totalWidth = width * unroll;

    size_t i = 0;
    simd_t max_v0(arr[0]);
    simd_t max_v1(arr[0]);
    simd_t max_v2(arr[0]);
    simd_t max_v3(arr[0]);

    for (; i + totalWidth <= arr.size(); i += totalWidth) {
        simd_t c0, c1, c2, c3;
        c0.copy_from(&arr[i], stdx::element_aligned);
        c1.copy_from(&arr[i + width], stdx::element_aligned);
        c2.copy_from(&arr[i + 2 * width], stdx::element_aligned);
        c3.copy_from(&arr[i + 3 * width], stdx::element_aligned);
        max_v0 = stdx::max(max_v0, c0);
        max_v1 = stdx::max(max_v1, c1);
        max_v2 = stdx::max(max_v2, c2);
        max_v3 = stdx::max(max_v3, c3);
    }

    simd_t max_v = stdx::max(stdx::max(max_v0, max_v1), stdx::max(max_v2, max_v3));

    for (; i + width <= arr.size(); i += width) {
        simd_t chunk;
        chunk.copy_from(&arr[i], stdx::element_aligned);
        max_v = stdx::max(max_v, chunk);
    }

    if (i < arr.size()) {
        size_t remainder = arr.size() - i;
        simd_t indices([](size_t idx) { return static_cast<int64_t>(idx); });
        mask_t tail_mask = indices < static_cast<int64_t>(remainder);
        simd_t chunk(std::numeric_limits<int64_t>::lowest());
        stdx::where(tail_mask, chunk).copy_from(&arr[i], stdx::element_aligned);
        max_v = stdx::max(max_v, chunk);
    }

    return stdx::hmax(max_v);
}

