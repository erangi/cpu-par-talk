int64_t findMax_std(std::vector<int64_t>& arr)
{
    /*
    std::max_element is notoriously difficult for a compiler's auto-vectorizer to optimize because of its strict behavior contract.

    It returns an iterator, not a value: The function cannot just find the highest number; it must return a pointer/iterator to the memory location of that number.

    The "First Occurrence" rule: The C++ standard mandates that if the maximum value appears multiple times in the array, std::max_element must return the first occurrence.

    To turn that into SIMD code, the compiler cannot just do a simple vertical vector comparison. It has to maintain a second vector register tracking the memory indices of those elements. When it finds a new maximum, it has to safely update the index register. If there is a tie between vector lanes, it has to execute masking math to ensure the lower index wins.

    When GCC 16's auto-vectorizer encounters the template layers generated by std::execution::unseq, it looks at that index-tracking and tie-breaking logic and almost always flags it as not vectorized: complex control flow or not vectorized: data dependency. It gives up and falls back to a standard sequential loop.
    */
    return *max_element(std::execution::unseq, arr.begin(), arr.end());
}

int64_t findMax_reduce(const std::vector<int64_t>& arr)
{
    /*
    You might wonder: If I told the algorithm to run sequentially (seq), why didn't it drop back down to the slow 477 ns scalar baseline?

    It didn't drop down because you didn't explicitly block vectorization on this new function. More importantly, std::reduce is mathematically allowed to cheat.

    Unlike std::accumulate (which strictly requires a left-to-right processing order), the C++ standard explicitly states that std::reduce is allowed to rearrange the order of operations however it sees fit, even when running sequentially.

    Because GCC 16 sees that the sequential order doesn't matter, its global loop optimizer says: "Excellent, I can auto-vectorize this loop entirely on my own terms."
    */
    return std::reduce(std::execution::unseq, arr.begin(), arr.end(), arr[0],
        [](int64_t a, int64_t b) {
            return std::max(a, b);
        });
}

__attribute__((target("avx512f,avx512vl")))
int64_t findMax_intrinsics(std::vector<int64_t>& arr)
{
    // Assume: size is a multiple of 8
    const std::size_t stride = 8;
    const std::size_t arrSize = arr.size();
    const int64_t* data = arr.data();
    __m512i vmax = _mm512_loadu_si512((__m512i*)data); // first 8 elements

    for (std::size_t i = stride; i < arrSize; i += stride) {
        __m512i v = _mm512_loadu_si512((__m512i*)(data + i));
        vmax = _mm512_max_epi64(vmax, v); // pairwise max of 8 lanes
    }

    // Horizontal reduction: collapse 8 lanes into 1
    // Step 1: split high and low halves
    __m256i lo256 = _mm512_castsi512_si256(vmax);
    __m256i hi256 = _mm512_extracti64x4_epi64(vmax, 1);
    __m256i max256 = _mm256_max_epi64(lo256, hi256);

    // Step 2: reduce 256-bit -> 128-bit
    __m128i lo128 = _mm256_castsi256_si128(max256);
    __m128i hi128 = _mm256_extracti128_si256(max256, 1);
    __m128i max128 = _mm_max_epi64(lo128, hi128);

    // Step 3: reduce 128-bit -> 64-bit
    __m128i hi64 = _mm_unpackhi_epi64(max128, max128);
    __m128i max64 = _mm_max_epi64(max128, hi64);

    return _mm_cvtsi128_si64(max64);
}

__attribute__((target("avx512f,avx512vl")))
int64_t findMax_intrinsics_unrolled(std::vector<int64_t>& arr)
{
    const std::size_t stride = 8;
    const std::size_t unroll = 4;
    const std::size_t totalStride = stride * unroll;
    const std::size_t arrSize = arr.size();
    const int64_t* data = arr.data();

    if (arrSize < totalStride) return findMax_intrinsics(arr);

    __m512i vmax0 = _mm512_loadu_si512((__m512i*)data);
    __m512i vmax1 = _mm512_loadu_si512((__m512i*)(data + stride));
    __m512i vmax2 = _mm512_loadu_si512((__m512i*)(data + 2 * stride));
    __m512i vmax3 = _mm512_loadu_si512((__m512i*)(data + 3 * stride));

    std::size_t i = totalStride;
    for (; i + totalStride <= arrSize; i += totalStride) {
        __m512i v0 = _mm512_loadu_si512((__m512i*)(data + i));
        __m512i v1 = _mm512_loadu_si512((__m512i*)(data + i + stride));
        __m512i v2 = _mm512_loadu_si512((__m512i*)(data + i + 2 * stride));
        __m512i v3 = _mm512_loadu_si512((__m512i*)(data + i + 3 * stride));
        vmax0 = _mm512_max_epi64(vmax0, v0);
        vmax1 = _mm512_max_epi64(vmax1, v1);
        vmax2 = _mm512_max_epi64(vmax2, v2);
        vmax3 = _mm512_max_epi64(vmax3, v3);
    }

    __m512i vmax = _mm512_max_epi64(_mm512_max_epi64(vmax0, vmax1), _mm512_max_epi64(vmax2, vmax3));

    for (; i + stride <= arrSize; i += stride) {
        __m512i v = _mm512_loadu_si512((__m512i*)(data + i));
        vmax = _mm512_max_epi64(vmax, v);
    }

    __m256i lo256 = _mm512_castsi512_si256(vmax);
    __m256i hi256 = _mm512_extracti64x4_epi64(vmax, 1);
    __m256i max256 = _mm256_max_epi64(lo256, hi256);
    __m128i lo128 = _mm256_castsi256_si128(max256);
    __m128i hi128 = _mm256_extracti128_si256(max256, 1);
    __m128i max128 = _mm_max_epi64(lo128, hi128);
    __m128i hi64 = _mm_unpackhi_epi64(max128, max128);
    __m128i max64 = _mm_max_epi64(max128, hi64);

    int64_t result = _mm_cvtsi128_si64(max64);
    for (; i < arrSize; ++i) {
        if (data[i] > result) result = data[i];
    }
    return result;
}

inline __attribute__((always_inline))
int64_t findMaxShared(std::vector<int64_t>& arr)
{
    int64_t max = arr[0];

    for (int64_t v : arr) {
        if (max < v)
            max = v;
    }
    return max;
}

__attribute__((target("avx512f")))
int64_t findMaxAvx512(std::vector<int64_t>& arr)
{
    return findMaxShared(arr);
}

__attribute__((target("avx2")))
int64_t findMaxAvx2(std::vector<int64_t>& arr)
{
    return findMaxShared(arr);
}

